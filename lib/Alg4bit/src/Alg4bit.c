#include "Alg4bit.h"

void SeleCombo(int count, char ret[10])
{
    switch (count){
case 0: strcpy(ret, "1cajon");
    break;
case 1: strcpy(ret, "2cajon");
    break;
case 2: strcpy(ret, "3cajon");
    break;
case 3: strcpy(ret, "4cajon");
    break;
case 4: strcpy(ret, "5cajon");
    break;
case 5: strcpy(ret, "6cajon");
    break;
default: strcpy(ret, "none");
}

    return;
}





